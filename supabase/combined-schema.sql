-- migrations/0004_scrape_requests.sql
-- ============================================================
-- Desktop users can request a novel that isn't scraped yet. The VM catalog
-- runner (`catalog-runner queue`) drains this table and scrapes the novels.

create table if not exists public.scrape_requests (
  id           bigint generated by default as identity primary key,
  novel_url    text not null,
  status       text not null default 'pending'
                 check (status in ('pending','processing','done','error')),
  requested_by uuid references auth.users(id) on delete set null,
  created_at   timestamptz not null default now(),
  processed_at timestamptz
);

-- one open request per URL at a time
create unique index if not exists uniq_pending_request
  on public.scrape_requests (novel_url)
  where status in ('pending', 'processing');

create index if not exists idx_scrape_requests_status on public.scrape_requests (status);

alter table public.scrape_requests enable row level security;

-- Authenticated users may file a request and see request status; the service
-- role (VM) updates rows (bypasses RLS).
drop policy if exists "insert own request" on public.scrape_requests;
create policy "insert own request" on public.scrape_requests
  for insert to authenticated
  with check (auth.uid() = requested_by);

drop policy if exists "read requests" on public.scrape_requests;
create policy "read requests" on public.scrape_requests
  for select to authenticated using (true);




-- ============ 0005_rankings.sql ============
-- External ranking lists scraped from webnoveldb.com (a WordPress novel
-- aggregator). Each row is one ranked title in a category; novel_id links it to
-- our novelfire catalog when a title match exists (NULL = we don't have it, the
-- UI greys it out). The VM replaces a category's rows on each scrape.

create table if not exists public.rankings (
  category    text   not null,            -- trending | daily | editors_choice | weekly | monthly | alltime
  position    integer not null,           -- 1-based rank within the category
  source_slug text   not null,            -- webnoveldb /novel/{slug}
  source_url  text   not null,
  title       text   not null,
  cover_url   text,
  novel_id    bigint references public.novels(id) on delete set null,
  scraped_at  timestamptz not null default now(),
  primary key (category, position)
);

create index if not exists idx_rankings_category on public.rankings (category, position);
create index if not exists idx_rankings_novel on public.rankings (novel_id);

alter table public.rankings enable row level security;

-- Read-all (anon + authenticated); writes are service-role only (the VM).
drop policy if exists rankings_read on public.rankings;
create policy rankings_read on public.rankings
  for select to anon, authenticated using (true);


-- ============ 0006_comments.sql ============
-- Novel comments scraped (read-only) from novelfire's /comment/show endpoint.
-- source_id = novelfire's data-comid, so re-scrapes dedupe instead of pile up.

create table if not exists public.comments (
  id         bigint generated by default as identity primary key,
  novel_id   bigint not null references public.novels(id) on delete cascade,
  source_id  text not null,
  author     text,
  avatar_url text,
  body       text not null,
  posted_at  text,             -- relative label as shown on novelfire ("12m", "3d")
  likes      integer not null default 0,
  scraped_at timestamptz not null default now(),
  unique (novel_id, source_id)
);

create index if not exists idx_comments_novel on public.comments (novel_id, id desc);

alter table public.comments enable row level security;

-- Read-all (anon + authenticated); writes are service-role only (the VM).
drop policy if exists comments_read on public.comments;
create policy comments_read on public.comments
  for select to anon, authenticated using (true);


-- ============ 0007_scrape_progress.sql ============
-- Aggregate scrape-progress counters, exposed as a single-row view so the app
-- can show live progress with the publishable key (read-only). Runs as the view
-- owner, so the distinct-novel counts work regardless of per-table RLS.

create or replace view public.scrape_progress as
select
  (select count(*) from public.novels)                              as novels_total,
  (select count(*) from public.novels where image_url is not null)  as novels_enriched,
  (select count(distinct novel_id) from public.chapters)            as novels_with_chapters,
  (select count(*) from public.chapters)                            as chapters_total,
  (select count(distinct novel_id) from public.comments)           as novels_with_comments,
  (select count(*) from public.comments)                            as comments_total,
  (select count(*) from public.scrape_requests where status = 'pending')    as queue_pending,
  (select count(*) from public.scrape_requests where status = 'processing') as queue_processing;

grant select on public.scrape_progress to anon, authenticated;


-- ============ 0008_hide_progress.sql ============
-- Hide scrape internals from the public REST surface. The app reads
-- scrape_progress over an *authenticated* anonymous session (role
-- `authenticated`), so the owner's admin dashboard keeps working; revoking
-- `anon` blocks reads made with only the publishable key and no session.
revoke select on public.scrape_progress from anon;
